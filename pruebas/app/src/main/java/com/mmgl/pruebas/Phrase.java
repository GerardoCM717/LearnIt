package com.mmgl.pruebas;

public class Phrase {
    private String spanish;
    private String english;

    public Phrase(String spanish, String english) {
        this.spanish = spanish;
        this.english = english;
    }

    public String getSpanish() {
        return spanish;
    }

    public String getEnglish() {
        return english;
    }
}
